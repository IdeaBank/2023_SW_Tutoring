#include <stdio.h>

int main()
{
    int arr[2] = { 0b00000001000000100000001100000100, 0b00000101000001100000011100001000 };
    // arr[0] = 00000001 00000010 00000011 00000100
    // arr[1] = 00000101 00000110 00000111 00001000

    // 00000001: 1, 00000010: 2, 00000011: 3, 00000100: 4
    // 00000101: 5, 00000110: 6, 00000111: 7, 00001000: 8

    char *b = (char *)(arr);

    char *c = b + 1;
    char *d = b + 2;
    char *e = b + 3;

    printf("%d %d %d %d\n", *b, *c, *d, *e);
    // 4 3 2 1 출력

    // Little Endian으로 저장되어 있어서 이러한 결과가 나오는 거임. 
    // ( 컴퓨터에 저장 시 역순으로 바이트씩 저장됨. 00000100 00000011 00000010 00000001 )

    // arr[0]에 저장되어 있는 값을 24131191로 바꾸자!

    // 참고) 24131191 = 0b00000001011100000011011001110111
    // 00000001 01110000 00110110 01110111
    // 1 112 54 119

    *b = 119;
    *c = 54;
    *d = 112;
    *e = 1;

    printf("%d\n", arr[0]);
}